								<p style="text-indent:35px;">Birbirinden güzel sokaklarıyla ve doğa harikalarıyla Venedik kalbimde unutulmaz bir yer bıraktı. Birçok filme ve kitaba konu olan güzelliklerin hiç de abartı olmadığını müşahede ettim. Bu yaz nereye gitsem diye kara kara düşünenler bana kesinlikle güvenebilir. Venedik Venedik Venedik…</p>
							
